//1.Write function to calc the area of a rectangle
function calcAreaOfRectangle(a, b) {
  if (a <= 0 || b <= 0) return -1;

  return a * b;
}
//2.perimeter of a rectangle
function calcPerimeterOfRectangle(a, b) {
  if (a <= 0 || b <= 0) return -1;

  return (a + b) * 2;
}
//3.the area of circle
function calcAreaOfRectangle(r) {
  if (r <= 0) return -1;

  return Math.PI * r * r;
}
